var monthNames = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December']

exports.dateParse = function (num) {
    var date = new Date(num*1000);
    var natural = monthNames[date.getUTCMonth()]+" "+date.getUTCDate()+", "+date.getUTCFullYear();
    return { "unix": num, "natural": natural }
};

exports.naturalDateParse = function (dateString) {
    var date = new Date(decodeURI(dateString));
    var natural = monthNames[date.getUTCMonth()]+" "+date.getUTCDate()+", "+date.getUTCFullYear();
    if (isNaN(date.getUTCMonth())) {return { "unix": "null", "natural": "null" }}
    else {return { "unix": Math.round(date/1000), "natural": natural }}
};